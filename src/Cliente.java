public class Cliente {

    private int id;
    private String nome;
    private String cpf;
    private String email;
    private String telefone;

    public Cliente(int id, String telefone, String email, String cpf, String nome) {
        this.id = id;
        this.telefone = telefone;
        this.email = email;
        this.cpf = cpf;
        this.nome = nome;
    }



}
